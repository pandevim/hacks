export const getRandomAlliterativeSentence = () => {
  const adverb = random(adverbs)
  const adjective = alliterativeRandom(adjectives, adverb)
  const noun = alliterativeRandom(nouns, adjective)

  return `${adverb} ${adjective} ${noun}`
}

const random = (words: Array<string>) => words[Math.round(Math.random() * (words.length - 1))]

const alliterativeRandom = (words: Array<string>, followingWord: string) =>
  random(words.filter((word) => word.startsWith(followingWord.charAt(0))))

